use ecommerce_online;

CREATE TABLE NHOMNGUOIDUNG (
    NNN_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NNN_TEN VARCHAR(20) NOT NULL
);

CREATE TABLE NHANVIEN (
    NV_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NNN_ID VARCHAR(10) NOT NULL,
    NV_TEN VARCHAR(30),
    NV_EMAIL VARCHAR(30) UNIQUE NOT NULL,
    NV_NGAYSINH DATE,
    NV_DIACHI LONG VARCHAR,
    NV_GIOITINH CHAR(10),
    NV_SDT CHAR(10),
    NV_MATKHAU VARCHAR(20) NOT NULL,
    CONSTRAINT FK_NHANVIEN_NNN FOREIGN KEY (NNN_ID) REFERENCES NHOMNGUOIDUNG(NNN_ID)
);

CREATE TABLE GIAOHANG (
    GH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    GH_NGAYGIAO DATE,
    GH_TRANGTHAI VARCHAR(20)
);

CREATE TABLE KHACHHANG (
    KH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    KH_TEN VARCHAR(30),
    KH_EMAIL VARCHAR(30) UNIQUE NOT NULL,
    KH_NGAYSINH DATE,
    KH_DIACHI LONG VARCHAR,
    KH_GIOITINH CHAR(10),
    KH_SDT CHAR(10),
    KH_MATKHAU VARCHAR(20) NOT NULL
);

CREATE TABLE LOAISANPHAM (
    LSP_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    LSP_TEN VARCHAR(30)
);

CREATE TABLE KHUYENMAI (
    KM_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    KM_TEN VARCHAR(50),
    KM_GIA DOUBLE
);

CREATE TABLE DANHMUC (
    DM_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    DM_TEN VARCHAR(30)
);


CREATE TABLE THUONGHIEU (
    TH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    TH_TEN VARCHAR(30)
);

CREATE TABLE SANPHAM (
    SP_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    LSP_ID VARCHAR(10) NOT NULL,
    KM_ID VARCHAR(10) NOT NULL,
    DM_ID VARCHAR(10) NOT NULL,
    TH_ID VARCHAR(10) NOT NULL,
    SP_TEN LONG VARCHAR NOT NULL,
    SP_GIA DOUBLE,
    SP_MOTA LONG VARCHAR NOT NULL,
    SP_SOLUONG INTEGER,
    CONSTRAINT FK_SANPHAM_LSP FOREIGN KEY (LSP_ID) REFERENCES LOAISANPHAM(LSP_ID),
    CONSTRAINT FK_SANPHAM_KM FOREIGN KEY (KM_ID) REFERENCES KHUYENMAI(KM_ID),
    CONSTRAINT FK_SANPHAM_DM FOREIGN KEY (DM_ID) REFERENCES DANHMUC(DM_ID),
    CONSTRAINT FK_SANPHAM_TH FOREIGN KEY (TH_ID) REFERENCES THUONGHIEU(TH_ID)
);

CREATE TABLE NHACUNGCAP (
    NCC_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NCC_TEN VARCHAR(30),
    NCC_DIACHI LONG VARCHAR,
    NCC_SDT CHAR(10),
    NCC_EMAIL VARCHAR(30)
);

CREATE TABLE HOADONDATHANG (
    HDDH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NV_ID VARCHAR(10) NOT NULL,
    KH_ID VARCHAR(10) NOT NULL,
    HDDH_NGAYDAT DATE,
    HDDH_DIACHI LONG VARCHAR NOT NULL,
    HDDH_TRANGTHAI VARCHAR(20),
    HDDH_TONGTIEN DOUBLE,
    HDDH_SDT CHAR(10) NOT NULL,
    HDDH_PTTT VARCHAR(50) NOT NULL,
    HDDH_GHICHU LONG VARCHAR,
    CONSTRAINT FK_HDDH_NV FOREIGN KEY (NV_ID) REFERENCES NHANVIEN(NV_ID),
    CONSTRAINT FK_HDDH_KH FOREIGN KEY (KH_ID) REFERENCES KHACHHANG(KH_ID)
);

CREATE TABLE CHITIETHDDH (
    HDDH_ID VARCHAR(10) NOT NULL,
    SP_ID VARCHAR(10) NOT NULL,
    CTHD_SOLUONG INTEGER,
    CTHD_GIA DOUBLE,
    PRIMARY KEY (HDDH_ID, SP_ID),
    CONSTRAINT FK_CTHDDH_HDDH FOREIGN KEY (HDDH_ID) REFERENCES HOADONDATHANG(HDDH_ID),
    CONSTRAINT FK_CTHDDH_SP FOREIGN KEY (SP_ID) REFERENCES SANPHAM(SP_ID)
);

CREATE TABLE GIOHANG (
    KH_ID VARCHAR(10) NOT NULL,
    SP_ID VARCHAR(10) NOT NULL,
    GH_SOLUONG INTEGER NOT NULL,
    PRIMARY KEY (KH_ID, SP_ID),
    CONSTRAINT FK_GIOHANG_KH FOREIGN KEY (KH_ID) REFERENCES KHACHHANG(KH_ID),
    CONSTRAINT FK_GIOHANG_SP FOREIGN KEY (SP_ID) REFERENCES SANPHAM(SP_ID)
);

CREATE TABLE HOADONGIAOHANG (
    HDGH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NV_ID VARCHAR(10) NOT NULL,
    GH_ID VARCHAR(10) NOT NULL,
    HDGH_NGAYLAP DATE,
    HDGH_DIACHI LONG VARCHAR NOT NULL,
    HDGH_TRANGTHAITHANHTOAN VARCHAR(20),
    HDGH_TONGTIEN DOUBLE,
    HDGH_SDT CHAR(10) NOT NULL,
    HDGH_GHICHU LONG VARCHAR,
    CONSTRAINT FK_HDGH_NV FOREIGN KEY (NV_ID) REFERENCES NHANVIEN(NV_ID),
    CONSTRAINT FK_HDGH_GH FOREIGN KEY (GH_ID) REFERENCES GIAOHANG(GH_ID)
);

CREATE TABLE CHITIETHOADONGIAOHANG (
    HDGH_ID VARCHAR(10) NOT NULL,
    SP_ID VARCHAR(10) NOT NULL,
    HDGH_SOLUONG INTEGER NOT NULL,
    HDGH_GIA DOUBLE NOT NULL,
    PRIMARY KEY (HDGH_ID, SP_ID),
    CONSTRAINT FK_CTHDGH_HDGH FOREIGN KEY (HDGH_ID) REFERENCES HOADONGIAOHANG(HDGH_ID),
    CONSTRAINT FK_CTHDGH_SP FOREIGN KEY (SP_ID) REFERENCES SANPHAM(SP_ID)
);

CREATE TABLE TINTUC (
    TT_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NV_ID VARCHAR(10) NOT NULL,
    TT_TEN VARCHAR(30),
    TT_NGAYDANG DATE,
    TT_NOIDUNG LONG VARCHAR,
    TT_ANHDAIDIEN LONG VARCHAR,
    CONSTRAINT FK_TINTUC_NV FOREIGN KEY (NV_ID) REFERENCES NHANVIEN(NV_ID)
);

CREATE TABLE LIENHE (
    LH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    LH_NGAYGIO DATE NOT NULL,
    LH_HOTEN VARCHAR(50),
    LH_SDT CHAR(10),
    LH_EMAIL VARCHAR(30)
);

CREATE TABLE PHANHOILIENHE (
    LH_ID VARCHAR(10) NOT NULL,
    NV_ID VARCHAR(10) NOT NULL,
    PHLH_NOIDUNG LONG VARCHAR,
    PHLH_NGAYGIO DATE,
    PRIMARY KEY (LH_ID, NV_ID),
    CONSTRAINT FK_PHLH_LH FOREIGN KEY (LH_ID) REFERENCES LIENHE(LH_ID),
    CONSTRAINT FK_PHLH_NV FOREIGN KEY (NV_ID) REFERENCES NHANVIEN(NV_ID)
);

CREATE TABLE KHO (
    K_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    K_TEN VARCHAR(30)
);

CREATE TABLE HOADONXUATHANG (
    HDXH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NV_ID VARCHAR(10) NOT NULL,
    K_ID VARCHAR(10) NOT NULL,
    HDXH_NGAYXUAT DATE,
    HDXH_GHICHU LONG VARCHAR,
    CONSTRAINT FK_HDXH_NV FOREIGN KEY (NV_ID) REFERENCES NHANVIEN(NV_ID),
    CONSTRAINT FK_HDXH_K FOREIGN KEY (K_ID) REFERENCES KHO(K_ID)
);

CREATE TABLE CHITIETHOADONXUATHANG (
    HDXH_ID VARCHAR(10) NOT NULL,
    SP_ID VARCHAR(10) NOT NULL,
    CTXH_SOLUONG INTEGER NOT NULL,
    PRIMARY KEY (HDXH_ID, SP_ID),
    CONSTRAINT FK_CTXH_HDXH FOREIGN KEY (HDXH_ID) REFERENCES HOADONXUATHANG(HDXH_ID),
    CONSTRAINT FK_CTXH_SP FOREIGN KEY (SP_ID) REFERENCES SANPHAM(SP_ID)
);

CREATE TABLE HOADONNHAPHANG (
    HDNH_ID VARCHAR(10) PRIMARY KEY NOT NULL,
    NCC_ID VARCHAR(10) NOT NULL,
    K_ID VARCHAR(10) NOT NULL,
    HDNH_NGAYNHAP DATE,
    HDNH_GHICHU LONG VARCHAR,
    CONSTRAINT FK_HDNH_NCC FOREIGN KEY (NCC_ID) REFERENCES NHACUNGCAP(NCC_ID),
    CONSTRAINT FK_HDNH_K FOREIGN KEY (K_ID) REFERENCES KHO(K_ID)
);

CREATE TABLE CHITIETHOADONNHAPHANG (
    HDNH_ID VARCHAR(10) NOT NULL,
    SP_ID VARCHAR(10) NOT NULL,
    CTNH_SOLUONG INTEGER NOT NULL,
    CTNH_GIA DOUBLE NOT NULL,
    PRIMARY KEY (HDNH_ID, SP_ID),
    CONSTRAINT FK_CTNH_HDNH FOREIGN KEY (HDNH_ID) REFERENCES HOADONNHAPHANG(HDNH_ID),
    CONSTRAINT FK_CTNH_SP FOREIGN KEY (SP_ID) REFERENCES SANPHAM(SP_ID)
